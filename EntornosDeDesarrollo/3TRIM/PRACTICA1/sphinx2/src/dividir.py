def division(num1, num2):
    """Esta función realiza la division de dos números

    Args:
        num1 (int): El primer número
        num2 (int): El segundo número
        
    Returns:
        int: La division de los dos números
        
    Raises:
        ZeroDivisionError: Si el divisor es cero.
    """
    resultado = num1 / num2
    return resultado