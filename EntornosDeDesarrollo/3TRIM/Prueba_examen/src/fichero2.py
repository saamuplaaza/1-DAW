def calculoTiempo(horasTotales, minutosTotales, segundosTotales):
    """Esta función hace el cálculo del tiempo total y lo imprime por pantalla.

    Args:
        horasTotales (int): total de horas
        minutosTotales (int): total de minutos
        segundosTotales (int): total de segundos

    Returns:
        list: horas, minutos y segundos
    """

    h,m,s = horasTotales, minutosTotales, segundosTotales
    return h, m, s